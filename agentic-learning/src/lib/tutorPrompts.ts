export type TutorType = 'tutor' | 'suggestions' | 'analytics' | 'report' | 'adaptive' | 'goals' | 'quiz' | 'spaced-repetition' | 'socratic' | 'study-plan' | 'gap-analysis';

export interface TutorProgressContext {
  completedLessons?: string[];
  quizScores?: { lessonId: string; score: number; total: number; timestamp: number }[];
  weakAreas?: string[];
  strongAreas?: string[];
  streak?: number;
  level?: number;
}

export interface TutorPageContext {
  page: string;
  subject?: { id: string; name: string };
  course?: { id: string; name: string };
  lesson?: { id: string; name: string };
}

