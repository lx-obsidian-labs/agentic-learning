export const CURATED_VIDEOS_BY_SUBJECT: Record<string, CuratedVideo[]> = CURATED_VIDEOS.reduce((acc, video) => {
  if (!acc[video.subject]) {
    acc[video.subject] = [];
  }
  acc[video.subject].push(video);
  return acc;
}, {} as Record<string, CuratedVideo[]>);

export const CURATED_VIDEOS_BY_TOPIC: Record<string, CuratedVideo[]> = CURATED_VIDEOS.reduce((acc, video) => {
  const topicKey = video.topic.toLowerCase().replace(/\s+/g, '-');
  if (!acc[topicKey]) {
    acc[topicKey] = [];
  }
  acc[topicKey].push(video);
  return acc;
}, {} as Record<string, CuratedVideo[]>);

export function getCuratedVideosBySubject(subjectId: string): CuratedVideo[] {
  return CURATED_VIDEOS_BY_SUBJECT[subjectId] || [];
}

export function getCuratedVideosByGrade(grade: number): CuratedVideo[] {
  return CURATED_VIDEOS.filter(v => v.grade === grade);
}

export function getCuratedVideosByTopic(topic: string): CuratedVideo[] {
  const topicKey = topic.toLowerCase().replace(/\s+/g, '-');
  return CURATED_VIDEOS_BY_TOPIC[topicKey] || [];
}

export function searchCuratedVideos(query: string): CuratedVideo[] {
  const queryLower = query.toLowerCase();
  return CURATED_VIDEOS.filter(video => 
    video.title.toLowerCase().includes(queryLower) ||
    video.description.toLowerCase().includes(queryLower) ||
    video.topic.toLowerCase().includes(queryLower) ||
    video.subject.toLowerCase().includes(queryLower)
  );
}
